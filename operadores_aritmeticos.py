# El programa pide los números al usuario
num1 = int(input("Ingresá un número (1): "))
num2 = int(input("Ingresá un número (2): "))

# Resultados

# Suma
print(f'La suma de ambos números es {num1 + num2}')
# Resta
print(f'La resta de ambos números es {num1 - num2}')
# Multiplicación
print(f'La multiplicación de ambos números es {num1 * num2}')
# División
print(f'La división de ambos números es {num1 / num2}')
# Módulo
print(f'El módulo de ambos números es {num1 % num2}')
